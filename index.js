// ‚Äî‚Äî‚Äî Single text handler for flow actions ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
bot.on('message', async (ctx, next) => {
  const id = ctx.from.id;
  const text = ctx.message.text && ctx.message.text.trim();
  if (!ctx.session) return next();

  await db.read();
  const user = db.data.users.find(u => u.id === id);
  if (!user) { ctx.reply('‚ùó Please /start to register.'); ctx.session = null; return; }

  // Deposit amount step
  if (ctx.session.action === 'await_deposit_amount' && text) {
    const amount = parseInt(text, 10);
    if (isNaN(amount) || amount <= 0) {
      ctx.reply('‚ùó Enter a valid positive number.');
      return;
    }
    // store deposit stub
    const reqId = Date.now();
    db.data.deposits.push({
      id: reqId, userId: id, amount, status: 'pending', screenshotFileId: null,
      timestamp: new Date().toISOString()
    });
    await db.write();
    ctx.reply('üì∏ Now please send a screenshot of your payment.');
    ctx.session = { action: 'await_deposit_screenshot', reqId };
    return;
  }

  // Withdraw amount step
  if (ctx.session.action === 'await_withdraw_amount' && text) {
    const amount = parseInt(text, 10);
    if (isNaN(amount) || amount <= 0 || user.coins < amount) {
      ctx.reply('‚ùó Invalid or insufficient balance.');
      ctx.session = null;
      return;
    }
    const reqId = Date.now();
    db.data.withdrawals.push({
      id: reqId, userId: id, amount, status: 'pending',
      timestamp: new Date().toISOString()
    });
    await db.write();
    ctx.reply('‚úÖ Withdrawal request submitted.');
    // notify admin
    await bot.telegram.sendMessage(
      ADMIN_ID,
      `üì§ Withdraw Request #${reqId}\nUser: ${user.name} (${id})\nAmount: ${amount} coins`,
      Markup.inlineKeyboard([
        Markup.button.callback('‚úÖ Approve', `approve_w_${reqId}`),
        Markup.button.callback('‚ùå Reject', `reject_w_${reqId}`)
      ])
    );
    ctx.session = null;
    return;
  }

  return next();
});

// ‚Äî‚Äî‚Äî Photo handler for deposit screenshot ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
bot.on('photo', async (ctx) => {
  if (!ctx.session || ctx.session.action !== 'await_deposit_screenshot') return;
  const { reqId } = ctx.session;
  const fileId = ctx.message.photo.slice(-1)[0].file_id;

  await db.read();
  const dep = db.data.deposits.find(d => d.id === reqId);
  if (!dep || dep.status !== 'pending') { ctx.session = null; return; }

  dep.screenshotFileId = fileId;
  await db.write();

  ctx.reply('üì® Deposit submitted for approval.');
  // notify admin with photo
  await bot.telegram.sendPhoto(
    ADMIN_ID,
    fileId,
    {
      caption: `üì• Deposit #${reqId}\nUser: ${ctx.from.first_name} (${ctx.from.id})\nAmount: ${dep.amount} coins`,
      ...Markup.inlineKeyboard([
        Markup.button.callback('‚úÖ Approve', `approve_d_${reqId}`),
        Markup.button.callback('‚ùå Reject', `reject_d_${reqId}`)
      ])
    }
  );
  ctx.session = null;
});

// ‚Äî‚Äî‚Äî Admin approval callbacks ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
bot.on('callback_query', async (ctx) => {
  const [action, type, rawId] = ctx.callbackQuery.data.split('_');
  const reqId = parseInt(rawId, 10);
  await db.read();

  // Deposit approval
  if (type === 'd') {
    const dep = db.data.deposits.find(d => d.id === reqId);
    if (!dep || dep.status !== 'pending') return ctx.answerCbQuery();
    const user = db.data.users.find(u => u.id === dep.userId);
    if (action === 'approve') {
      dep.status = 'approved';
      user.coins += dep.amount;
      await db.write();
      ctx.editMessageCaption(`‚úÖ Deposit #${reqId} approved.`);
      bot.telegram.sendMessage(user.id, `üéâ Your deposit of ${dep.amount} coins was approved!`);
    } else {
      dep.status = 'rejected';
      await db.write();
      ctx.editMessageCaption(`‚ùå Deposit #${reqId} rejected.`);
      bot.telegram.sendMessage(dep.userId, `‚ùå Your deposit of ${dep.amount} coins was rejected.`);
    }
    return ctx.answerCbQuery();
  }

  // Withdraw approval
  if (type === 'w') {
    const wd = db.data.withdrawals.find(w => w.id === reqId);
    if (!wd || wd.status !== 'pending') return ctx.answerCbQuery();
    const user = db.data.users.find(u => u.id === wd.userId);
    if (action === 'approve') {
      wd.status = 'approved';
      user.coins -= wd.amount;
      await db.write();
      ctx.editMessageText(`‚úÖ Withdrawal #${reqId} approved.`);
      bot.telegram.sendMessage(user.id, `‚úÖ Your withdrawal of ${wd.amount} coins was approved!`);
    } else {
      wd.status = 'rejected';
      await db.write();
      ctx.editMessageText(`‚ùå Withdrawal #${reqId} rejected.`);
      bot.telegram.sendMessage(wd.userId, `‚ùå Your withdrawal of ${wd.amount} coins was rejected.`);
    }
    return ctx.answerCbQuery();
  }
});

