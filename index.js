const { Telegraf, Markup } = require("telegraf");
const express = require("express");
require("dotenv").config();
const { Low } = require("lowdb");
const { JSONFile } = require("lowdb/node");

// ‚îÄ‚îÄ‚îÄ Server Keep‚ÄëAlive ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const app = express();
app.get("/", (_req, res) => res.send("ü§ñ Hanibal Bot is alive!"));
app.listen(3000, () => console.log("üåê Web server running on port 3000"));

// ‚îÄ‚îÄ‚îÄ Constants ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const ADMIN_ID = process.env.ADMIN_ID;
const COIN_VALUE_BIRR = 1;

// ‚îÄ‚îÄ‚îÄ LowDB Setup ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const adapter = new JSONFile("db.json");
const db = new Low(adapter, { users: [], withdrawals: [], deposits: [] });

// ‚îÄ‚îÄ‚îÄ In‚ÄëMemory States ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
const pendingOTPs = {};
const pendingDeposits = {};
const pendingWithdrawals = {};
const pendingAddCoins = {};

(async () => {
  // Ensure DB file exists
  await db.read();
  await db.write();

  const bot = new Telegraf(process.env.BOT_TOKEN);

  // ‚îÄ‚îÄ‚îÄ /start ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.start(async (ctx) => {
    const name = ctx.from.first_name;
    // Build the base menu
    const menu = [
      ["üí∞ Deposit Money", "üí∏ Withdraw Money"],
      ["üíº Check Balance", "üìù Register"],
      ["üì¢ Referral Link", "üîç My ID"],
      ["üí± Coin Rates"],
    ];

    // If the user is admin, add the Admin button
    if (ctx.from.id.toString() === ADMIN_ID) {
      menu.push(["üõ† Admin"]);
    }

    ctx.reply(
      `üëã Welcome, ${name}!\nüí∞ 1 Coin = ${COIN_VALUE_BIRR} Birr\nUse the menu below:`,
      Markup.keyboard(menu).resize()
    );
  });

  // ‚îÄ‚îÄ‚îÄ Admin button handler ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.hears("üõ† Admin", (ctx) => {
    if (ctx.from.id.toString() !== ADMIN_ID) return;
    ctx.reply(
      "üõ† Admin Tools",
      Markup.inlineKeyboard([
        [Markup.button.callback("üìã View Users", "view_users")],
        [Markup.button.callback("üìÑ View Deposits", "view_deposits")],
        [Markup.button.callback("üìÑ View Withdrawals", "view_withdrawals")],
        [Markup.button.callback("‚ûï Add Coins", "add_coins")],
      ])
    );
  });

  // ‚îÄ‚îÄ‚îÄ Coin Rates, My ID, Register ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.hears("üí± Coin Rates", (ctx) =>
    ctx.reply(`üí∞ 1 Coin = ${COIN_VALUE_BIRR} Birr`)
  );
  bot.hears("üîç My ID", (ctx) =>
    ctx.reply(`üÜî Your Telegram ID is: ${ctx.from.id}`)
  );
  bot.hears("üìù Register", async (ctx) => {
    const id = ctx.from.id;
    await db.read();
    if (db.data.users.find((u) => u.id === id)) {
      return ctx.reply("‚úÖ You are already registered.");
    }
    const otp = Math.floor(1000 + Math.random() * 9000).toString();
    pendingOTPs[id] = otp;
    ctx.reply(`üì® Your OTP is: ${otp}\nPlease reply with it to register.`);
  });

  // ‚îÄ‚îÄ‚îÄ Core text handler ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.on("text", async (ctx, next) => {
    const id = ctx.from.id;
    const txt = ctx.message.text.trim();
    const name = ctx.from.first_name;
    const username = ctx.from.username || "none";

    // OTP
    if (pendingOTPs[id]) {
      if (txt === pendingOTPs[id]) {
        await db.read();
        db.data.users.push({ id, name, username, coins: 0, referredBy: null });
        delete pendingOTPs[id];
        await db.write();
        return ctx.reply("‚úÖ Registration complete! You have 0 coins.");
      } else {
        return ctx.reply("‚ùó Wrong OTP. Try again.");
      }
    }

    // Deposit request
    if (pendingDeposits[id]) {
      const amt = parseFloat(txt);
      if (isNaN(amt) || amt <= 0) {
        delete pendingDeposits[id];
        return ctx.reply("‚ùó Invalid amount.");
      }
      await db.read();
      db.data.deposits.push({ userId: id, username, amount: amt, status: "pending" });
      delete pendingDeposits[id];
      await db.write();
      ctx.reply(`üí∏ Deposit of ${amt} coins requested. Awaiting admin.`);
      if (ADMIN_ID) {
        const idx = db.data.deposits.length - 1;
        await bot.telegram.sendMessage(
          ADMIN_ID,
          `üì• Deposit #${idx} from @${username} Amount: ${amt}`,
          Markup.inlineKeyboard([
            Markup.button.callback(`‚úÖ Approve #${idx}`, `approve_d_${idx}`),
            Markup.button.callback(`‚ùå Decline #${idx}`, `decline_d_${idx}`)
          ])
        );
      }
      return;
    }

    // Withdrawal request
    if (pendingWithdrawals[id]) {
      const amt = parseFloat(txt);
      await db.read();
      const user = db.data.users.find((u) => u.id === id);
      if (isNaN(amt) || amt <= 0 || !user || user.coins < amt) {
        delete pendingWithdrawals[id];
        return ctx.reply("‚ùó Invalid or insufficient balance.");
      }
      db.data.withdrawals.push({ userId: id, username, amount: amt, status: "pending" });
      delete pendingWithdrawals[id];
      await db.write();
      ctx.reply(`üí∏ Withdrawal of ${amt} coins requested. Awaiting admin.`);
      if (ADMIN_ID) {
        const idx = db.data.withdrawals.length - 1;
        await bot.telegram.sendMessage(
          ADMIN_ID,
          `üì§ Withdrawal #${idx} @${username} Amount: ${amt}`,
          Markup.inlineKeyboard([
            Markup.button.callback(`‚úÖ Approve #${idx}`, `approve_w_${idx}`),
            Markup.button.callback(`‚ùå Decline #${idx}`, `decline_w_${idx}`)
          ])
        );
      }
      return;
    }

    // Admin add coins
    if (pendingAddCoins[id]) {
      const [tid, coins] = txt.split(" ");
      const amt = parseInt(coins, 10);
      delete pendingAddCoins[id];
      await db.read();
      const u = db.data.users.find((u) => u.id.toString() === tid);
      if (!u || isNaN(amt)) {
        return ctx.reply("‚ùó Format: userID amount");
      }
      u.coins += amt;
      await db.write();
      return ctx.reply(`‚úÖ Added ${amt} coins to ${u.name}`);
    }

    return next();
  });

  // ‚îÄ‚îÄ‚îÄ Other Listeners ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.hears("üíº Check Balance", async (ctx) => {
    const id = ctx.from.id;
    await db.read();
    const u = db.data.users.find((u) => u.id === id);
    if (!u) return ctx.reply("‚ùó Register first with üìù Register.");
    ctx.reply(`üí∞ Balance: ${u.coins} coins`);
  });

  bot.hears("üì¢ Referral Link", async (ctx) => {
    const id = ctx.from.id;
    await db.read();
    if (!db.data.users.find((u) => u.id === id)) {
      return ctx.reply("‚ùó Register first with üìù Register.");
    }
    const link = `https://t.me/${bot.botInfo.username}?start=${id}`;
    ctx.reply(`üì¢ Share this link:\n${link}`);
  });

  bot.hears("üí∞ Deposit Money", (ctx) => {
    const id = ctx.from.id;
    pendingDeposits[id] = true;
    ctx.reply("üí∏ Enter how many coins to deposit:");
  });

  bot.hears("üí∏ Withdraw Money", (ctx) => {
    const id = ctx.from.id;
    pendingWithdrawals[id] = true;
    ctx.reply("üí∏ Enter how many coins to withdraw:");
  });

  // ‚îÄ‚îÄ‚îÄ Admin Inline Handlers ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.action("view_users", async (ctx) => {
    if (ctx.from.id.toString() !== ADMIN_ID) return;
    await db.read();
    const list =
      db.data.users
        .map((u) => `üë§ ${u.name} (@${u.username}) ‚Äî ${u.coins} coins`)
        .join("\n") || "No users.";
    ctx.reply(list);
  });

  bot.action("view_deposits", async (ctx) => {
    if (ctx.from.id.toString() !== ADMIN_ID) return;
    await db.read();
    const list =
      db.data.deposits
        .map((d, i) => `${i}. @${d.username} ‚Äî ${d.amount} coins ‚Äî ${d.status}`)
        .filter((l) => l.includes("pending"))
        .join("\n") || "No pending deposits.";
    ctx.reply(list);
  });

  bot.action("approve_d_(\\d+)", async (ctx) => {
    if (ctx.from.id.toString() !== ADMIN_ID) return;
    const idx = parseInt(ctx.match[1], 10);
    await db.read();
    const d = db.data.deposits[idx];
    if (!d || d.status !== "pending") return ctx.reply("Invalid request.");
    d.status = "approved";
    const u = db.data.users.find((u) => u.id === d.userId);
    if (u) u.coins += d.amount;
    await db.write();
    await ctx.reply(`‚úÖ Deposit #${idx} approved.`);
    await bot.telegram.sendMessage(d.userId, `‚úÖ Your deposit of ${d.amount} coins was approved.`);
  });

  bot.action("decline_d_(\\d+)", async (ctx) => {
    if (ctx.from.id.toString() !== ADMIN_ID) return;
    const idx = parseInt(ctx.match[1], 10);
    await db.read();
    const d = db.data.deposits[idx];
    if (!d || d.status !== "pending") return ctx.reply("Invalid request.");
    d.status = "declined";
    await db.write();
    await ctx.reply(`‚ùå Deposit #${idx} declined.`);
    await bot.telegram.sendMessage(d.userId, `‚ùå Your deposit of ${d.amount} coins was declined.`);
  });

  // ‚îÄ‚îÄ‚îÄ Launch Bot ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
  bot.launch();
  console.log("ü§ñ Bot is running...");
})();
