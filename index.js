// ‚Äî‚Äî‚Äî Helpers ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
function ensureUser(id, first_name, username, referrerId) {
  const exists = db.data.users.find(u => u.id === id);
  if (!exists) {
    db.data.users.push({
      id,
      name: first_name,
      username: username || '',
      coins: 0,
      referredBy: referrerId || null
    });
  }
}
function listUsers() {
  return db.data.users.map(u => `üë§ ${u.name} (@${u.username}) ‚Äî ${u.coins} coins`).join('\n') || 'No users yet.';
}
function listPending() {
  const deps = db.data.deposits.filter(d => d.status === 'pending');
  const wds = db.data.withdrawals.filter(w => w.status === 'pending');
  let msg = '';
  if (deps.length) {
    msg += 'üü¢ *Pending Deposits:*\n';
    deps.forEach(d => msg += `‚Ä¢ #${d.id} by ${d.userId} ‚Äî ${d.amount} coins\n`);
    msg += '\n';
  }
  if (wds.length) {
    msg += 'üî¥ *Pending Withdrawals:*\n';
    wds.forEach(w => msg += `‚Ä¢ #${w.id} by ${w.userId} ‚Äî ${w.amount} coins\n`);
  }
  return msg || 'No pending requests.';
}

// ‚Äî‚Äî‚Äî /start with referral support ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
bot.start(async (ctx) => {
  const id = ctx.from.id;
  const ref = ctx.startPayload;  // payload from /start?start=<ref>
  await db.read();
  ensureUser(id, ctx.from.first_name, ctx.from.username, ref);
  await db.write();

  // Build menu
  const isAdmin = id.toString() === ADMIN_ID;
  const menu = [
    ['üìù Register', 'üíº Check Balance'],
    ['üí∞ Deposit Money', 'üí∏ Withdraw Money'],
    ['üì¢ Referral Link', 'üîç My ID'],
    ['üìä Transactions']
  ];
  if (isAdmin) menu.push(['üõ† Admin Tools']);

  await ctx.reply(
    `üëã Welcome, ${ctx.from.first_name}!\n` +
    `üí∞ 1 Coin = ${COIN_VALUE_BIRR} Birr\n` +
    `Use the menu below:`,
    Markup.keyboard(menu).resize()
  );
});

// ‚Äî‚Äî‚Äî User Commands ‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî‚Äî
// /referrals ‚Äî list users referred by me
